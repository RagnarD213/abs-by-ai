  const JOINTS = ['nk','hp']; // torso
  function buildAnimSvg(frames, size) {
    // Loop back to the first frame; 3-frame sets cycle 0→1→2→0.
    const seqFrames = frames.concat([frames[0]]);
    const n = seqFrames.length;
    const keyTimes = seqFrames.map((_, i) => (i / (n - 1)).toFixed(3)).join(';');
    const splines = seqFrames.slice(1).map(() => '0.42 0 0.58 1').join(';');
    const dur = frames.length > 2 ? '2.6s' : '2s';
    const anim = (attr, fn) =>
      `<animate attributeName="${attr}" values="${seqFrames.map(fn).join(';')}" keyTimes="${keyTimes}" dur="${dur}" repeatCount="indefinite" calcMode="spline" keySplines="${splines}"/>`;
    const poly = (keys) => {
      const pts = (f) => keys.map((k) => f[k].join(',')).join(' ');
      return `<polyline points="${pts(seqFrames[0])}" fill="none">${anim('points', pts)}</polyline>`;
    };
    return `<svg class="ex-anim" viewBox="0 0 120 120" width="${size}" height="${size}" xmlns="http://www.w3.org/2000/svg" stroke="currentColor" stroke-width="5" stroke-linecap="round" stroke-linejoin="round" fill="none" aria-hidden="true">` +
      `<circle r="7.5" cx="${seqFrames[0].hd[0]}" cy="${seqFrames[0].hd[1]}">${anim('cx', (f) => f.hd[0])}${anim('cy', (f) => f.hd[1])}</circle>` +
      poly(['nk', 'hp']) +
      poly(['hp', 'k1', 'f1']) + poly(['hp', 'k2', 'f2']) +
      poly(['nk', 'e1', 'w1']) + poly(['nk', 'e2', 'w2']) +
      `<line x1="8" y1="114" x2="112" y2="114" stroke-width="2.5" opacity="0.3"/></svg>`;
  }

  const cache = {};
  function getExerciseAnim(id, size) {
    const key = `${id}@${size || 120}`;
    if (cache[key]) return cache[key];
    const poseName = ANIM_MAP[id];
    const frames = poseName && POSES[poseName];
    if (!frames) return '';
    return (cache[key] = buildAnimSvg(frames, size || 120));
  }

